const numberMonth = prompt("Type a month number (1-12). ");

if (numberMonth == 12) {
  console.log("December");
} else if (numberMonth == 11) {
  console.log("November");
} else if (numberMonth == 10) {
  console.log("October");
} else if (numberMonth == 9) {
  console.log("September");
} else if (numberMonth == 8) {
  console.log("August");
} else if (numberMonth == 7) {
  console.log("July");
} else if (numberMonth == 6) {
  console.log("June");
} else if (numberMonth == 5) {
  console.log("May");
} else if (numberMonth == 4) {
  console.log("April");
} else if (numberMonth == 3) {
  console.log("March");
} else if (numberMonth == 2) {
  console.log("Febuary");
} else if (numberMonth == 1) {
  console.log("January");
} else {
  console.log("That is not a month");
}
